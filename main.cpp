void uart_print_float(double value, int dec_digits)
{
	// Function to print to console (screen tty 115200 on Mac)
	int whole_part = value;
	double dec_part = (value - whole_part) ;
	double idp = (dec_part * std::pow(10, dec_digits));
	int flo = std::floor(idp);
	std::string whole_str = std::to_string(whole_part);
	std::string dec_str = std::to_string(flo);
	std::string new_str = whole_str + "." +dec_str + "\n\r";

	int total_len = new_str.length();
	unsigned char* char_rep = new unsigned char[total_len];

	for(uint16_t i=0; i<new_str.length(); i++)
	{
		char_rep[i] = new_str[i];
	}

	HAL_UART_Transmit(&huart3,  (const uint8_t*) char_rep, (uint16_t) new_str.length(), (uint32_t)10);

	delete[] char_rep;
}


void uart_print_test_results(UnitTestSuite u)
{
	std::string results = "UNIT TESTS OUTPUT: \n\r";
	for(int i=0; i<u.get_num_tests(); i++)
	{
		results += "Test: " + std::to_string(i) + " result: " + std::to_string(u.tests_status[i]) + "\n\r";
	}

	int total_len = results.length();
	unsigned char* char_rep = new unsigned char[total_len];

	for(uint16_t i=0; i<total_len; i++)
	{
		char_rep[i] = results[i];
	}

	HAL_UART_Transmit(&huart3,  (const uint8_t*) char_rep, (uint16_t) total_len, (uint32_t)1000);

	delete[] char_rep;
}
